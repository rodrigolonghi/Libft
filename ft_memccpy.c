#include "libft.h"

void	*ft_memccpy(void *restrict d, const void *restrict s, int c, size_t n)
{
	size_t	i;

	i = 0;
	while (i < n)
	{
		if (((unsigned char *)s)[i] == (unsigned char)c)
			return ((void *)d + i + 1);
		((unsigned char *)d)[i] = ((unsigned char *)s)[i];
		i++;
	}
	return ((void *)d);
}
